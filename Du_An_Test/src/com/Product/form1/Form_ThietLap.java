    private void btn_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xoaActionPerformed
        // TODO add your handling code here:
     i=tbl_taiKhoan.getSelectedRow();
      if(i<0){
          JOptionPane.showMessageDialog(this, "Bạn chưa chọn dòng cần xóa");
      }
      else{
          int chon=JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn xóa ?");
          if(chon==0){
              int id=Integer.parseInt(tbl_taiKhoan.getValueAt(i, 0).toString());
              rp.deleteThietLap(id);
              JOptionPane.showMessageDialog(this, "Xóa thành công");
              this.fillTable(rp.getAll());
          }
          else{
              JOptionPane.showMessageDialog(this, "Bạn không chọn xóa");
          }
      }

    }//GEN-LAST:event_btn_xoaActionPerformed

    private void btn_suaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_suaActionPerformed
        // TODO add your handling code here:
                i=tbl_taiKhoan.getSelectedRow();
        if(i<0){
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn");
        }
        else{
            if(this.readForm1()!=null){
                int chon=JOptionPane.showConfirmDialog(this, "Bạn chắc chắn sửa?");
                if(chon==0){
                    int id=Integer.parseInt(tbl_taiKhoan.getValueAt(i, 0).toString());
                    rp.updateThietLap(id, this.readForm1());
                    JOptionPane.showMessageDialog(this, "Sửa thành công");
                    this.fillTable(rp.getAll());
                }
            }
        }
    }//GEN-LAST:event_btn_suaActionPerformed

