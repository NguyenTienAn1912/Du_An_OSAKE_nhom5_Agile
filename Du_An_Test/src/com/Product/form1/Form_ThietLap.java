        void showData(int index) {       
        String id;
        String maNV;
        String tenNV;
        String tenDangNhap;
        String matKhau;
        String phanQuyen;
        String tinhTrang;
        id = tbl_taiKhoan.getValueAt(index, 0).toString();
        maNV = tbl_taiKhoan.getValueAt(index, 1).toString();
        tenNV = tbl_taiKhoan.getValueAt(index, 2).toString();
        tenDangNhap = tbl_taiKhoan.getValueAt(index, 3).toString();
        matKhau = tbl_taiKhoan.getValueAt(index, 4).toString();
        phanQuyen = tbl_taiKhoan.getValueAt(index, 5).toString();
        tinhTrang = tbl_taiKhoan.getValueAt(index, 6).toString();
        txt_ID.setText(id);
        txt_maNV.setText(maNV);
        txt_hoTen.setText(tenNV);
        txt_tenDangNhap.setText(tenDangNhap);
        txt_matKhau.setText(matKhau);
        
        if (phanQuyen.equalsIgnoreCase("QL")) {
            rdo_quanLy.setSelected(true);
        } else {
            rdo_nhanVien.setSelected(true);
        }
        if (tinhTrang.equalsIgnoreCase("HĐ")) {
            rdo_hoatDong.setSelected(true);
        } else {
            rdo_ngungHoatDong.setSelected(true);
        }
    }

