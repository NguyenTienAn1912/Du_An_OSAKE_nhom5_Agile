    public void loaddata() {
        String[] header = {"Mã Nhà CC", "Tên Nhà CC ","Địa Chỉ ","Số điện thoại ","Trạng thái "};
        DefaultTableModel model = new DefaultTableModel(header, 0);
        tbl_NhaCC.setModel(model);
        
        for (Model.Model_NhaCC ncc : lst) {      
            Object[] row = {ncc.getMaNCC(),ncc.getTenNCC(),ncc.getDiaChi(),ncc.getSoDienThoai(),ncc.isTrangThai()?"Đối tác ":"Nhà cung cấp sỉ lẻ "};
            model.addRow(row); 
        }
    }
    
    public boolean checkMa(String ma){
        
        for (Model.Model_NhaCC ncc : lst) {
            if(ncc.getMaNCC().equals(ma)){
                return true;
            }                
        }
       return false;
    }
    
    public boolean checkTen(String ten){
        
        for (Model.Model_NhaCC ncc : lst) {
            if(ncc.getTenNCC().equals(ten)){
                return true;
            }                
        }
       return false;
    }
    
    
    
    public boolean checkSDT(String sdt){
        
        for (Model.Model_NhaCC ncc : lst) {
            if(ncc.getSoDienThoai().equals(sdt)){
                
                return true;
            }                
        }
       return false;
    }
    
    

