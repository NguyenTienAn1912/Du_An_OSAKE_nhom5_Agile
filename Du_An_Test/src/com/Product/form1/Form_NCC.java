    private void tbl_NhaCCMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_NhaCCMouseClicked
        int selectedRow = tbl_NhaCC.getSelectedRow();        
        
        if(selectedRow >= 0){
           Model.Model_NhaCC ncc = lst.get(selectedRow);
           txt_MaNCC.setText(ncc.getMaNCC());
           txt_HoTen.setText(ncc.getTenNCC());
           txt_SDT.setText(ncc.getSoDienThoai());
           txt_DiaChi.setText(ncc.getDiaChi());
           if (ncc.isTrangThai()) {
                buttonGroup1.setSelected(jRadioButton1.getModel(), true);
                buttonGroup1.setSelected(jRadioButton2.getModel(), false);
            } else {
                buttonGroup1.setSelected(jRadioButton1.getModel(), false);
                buttonGroup1.setSelected(jRadioButton2.getModel(), true);
            }
        }
        tbl_NhaCC.addMouseListener(new java.awt.event.MouseAdapter() {
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                tbl_NhaCCMouseClicked(evt);
            }
        }
        );
    }//GEN-LAST:event_tbl_NhaCCMouseClicked

    private void btn_ThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ThemActionPerformed
        
        boolean check = true;
        Model.Model_NhaCC ncc = new Model.Model_NhaCC();
        if(checkMa(txt_MaNCC.getText())){
            JOptionPane.showMessageDialog(rootPane, "Mã nhà cung cấp đã tồn tại ");
            check = false;
        }
        else if(txt_HoTen.getText().isEmpty()){
                JOptionPane.showMessageDialog(rootPane, "Tên nhà cung cấp không được để trống");
                txt_HoTen.requestFocus();
                check = false;
        } else if (checkTen(txt_HoTen.getText())){
                JOptionPane.showMessageDialog(rootPane, "Tên nhà cung cấp đã tồn tại ");
                txt_HoTen.requestFocus();
                check = false;
        }
        
        else if(txt_DiaChi.getText().isEmpty()){
                JOptionPane.showMessageDialog(rootPane, "Địa chỉ không được để trống");
                txt_DiaChi.requestFocus();
                check = false;
            }
        
        else if(txt_SDT.getText().isEmpty()){
                JOptionPane.showMessageDialog(rootPane, "Số điện thoại không được để trống");
                txt_SDT.requestFocus();
                check = false;
            }else if(checkSDT(txt_SDT.getText())){
                JOptionPane.showMessageDialog(rootPane, "Số điện thoại đã tồn tại !!");
                txt_SDT.requestFocus();
                check = false;
            }
        
        if(check == true){
            
            int chon = JOptionPane.showConfirmDialog(this, "Bạn có muốn thêm không?");
            if(chon == 0){
            ncc.setTenNCC(txt_HoTen.getText());
            ncc.setDiaChi(txt_DiaChi.getText());
            ncc.setSoDienThoai(txt_SDT.getText());
            ncc.setTrangThai(jRadioButton1.isSelected());
             try {
                repo.insert(ncc);
                getdata();
                loaddata();
                JOptionPane.showMessageDialog(rootPane, "Đã thêm nhà cung cấp mới thành công");
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane, "Đã thêm nhà cung cấp mới thất bại");
                Logger.getLogger(Form_KhuyenMai.class.getName()).log(Level.SEVERE, null, ex);
            }
             
            }else{
                JOptionPane.showMessageDialog(this, "Không thêm!");
            }
            
        }
        
    }//GEN-LAST:event_btn_ThemActionPerformed

    private void btn_SuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SuaActionPerformed
          int index = tbl_NhaCC.getSelectedRow();
          boolean check = true ;
          if(txt_HoTen.getText().isEmpty()){
                JOptionPane.showMessageDialog(rootPane, "Tên nhà cung cấp không được để trống");
                txt_HoTen.requestFocus();
                check = false;
        } else if (checkTen(txt_HoTen.getText())){
                JOptionPane.showMessageDialog(rootPane, "Tên nhà cung cấp đã tồn tại ");
                txt_HoTen.requestFocus();
                check = false;
        }
        
        if(txt_DiaChi.getText().isEmpty()){
                JOptionPane.showMessageDialog(rootPane, "Địa chỉ không được để trống");
                txt_DiaChi.requestFocus();
                check = false;
            }
        
        if(txt_SDT.getText().isEmpty()){
                JOptionPane.showMessageDialog(rootPane, "Số điện thoại không được để trống");
                txt_SDT.requestFocus();
                check = false;
            }
//        else 
//            if(checkSDT(txt_SDT.getText())){
//                JOptionPane.showMessageDialog(rootPane, "Số điện thoại đã tồn tại !!");
//                txt_SDT.requestFocus();
//                check = false;
//            }
              
                 
            if(index == -1){
            JOptionPane.showMessageDialog(rootPane, "Chưa chọn dòng để cập nhật");
            }else if (check == true){
            int chon = JOptionPane.showConfirmDialog(this, "Bạn có muốn thay đổi không?");
            if(chon == 0){
                Model.Model_NhaCC ncc = lst.get(index);    
            ncc.setTenNCC(txt_HoTen.getText());
            ncc.setDiaChi(txt_DiaChi.getText());
            ncc.setSoDienThoai(txt_SDT.getText());
            ncc.setTrangThai(jRadioButton1.isSelected());
           try {
                int result = repo.update(ncc);
                if (result > 0) {
                    JOptionPane.showMessageDialog(rootPane, "Cập nhật nhà cung cấp thành công");
                    getdata();
                    loaddata();
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Cập nhật nhà cung cấp thất bại");
                }
            } catch (SQLException ex) {
                Logger.getLogger(Form_KhuyenMai.class.getName()).log(Level.SEVERE, null, ex);
            }
            }else{
                JOptionPane.showMessageDialog(this, "Không cập nhập thông tin!");
            }
        }
    }//GEN-LAST:event_btn_SuaActionPerformed

