    public void loaddata() {
        String[] header = {"Mã voucher","Tên","Mức giảm","Điều kiện ","Số lần","Ngày bắt đầu","Ngày kết thúc","Trạng thái", "Ngày tạo","Ngày cập nhật"};
        DefaultTableModel model = new DefaultTableModel(header, 0);
        tbl_DanhSachKM.setModel(model);
        Date dateNow = new Date();
        int trangThai = 0;
        
        for (Model.Model_KhuyenMai km : lst) {
            if(km.getNgay_bat_dau().after(dateNow) && km.getNgay_het_han().before(dateNow)){
                trangThai = 1;
            }
            Object[] row = {km.getMa_voucher(),km.getTen_giam_gia(), Math.round(km.getPhan_tram_giam_gia()) + "%",
                String.format("%,.3f VNĐ ", km.getDieu_kien_ap_dung()),
                km.getSo_lan_su_dung(),
                km.getNgay_bat_dau(),
                km.getNgay_het_han(),
                trangThai == 0?"Hết Hạn":"Đang áp dụng ",
                km.getNgay_tao(),
                km.getNgay_cap_nhat()};
            model.addRow(row);
            
        }
        
        
    }
    
    public boolean checkMa(String ma){
        
        for (Model.Model_KhuyenMai km : lst) {
            if(km.getMa_voucher().equals(ma)){
                return true;
            }                
        }
       return false;
    }
    
    public boolean checkTen(String ten){
        
        for (Model.Model_KhuyenMai km : lst) {
            if(km.getTen_giam_gia().equals(ten)){
                return true;
            }                
        }
       return false;
    }
