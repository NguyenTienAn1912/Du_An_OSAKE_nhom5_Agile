    private void btn_ThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ThemActionPerformed
            boolean check = true ;
            Model.Model_KhuyenMai km = new Model.Model_KhuyenMai();
            
            String dateBD =  new SimpleDateFormat("yyyy-MM-dd").format(ngayBatDau.getDate());
            java.sql.Date dateBD2 = java.sql.Date.valueOf(dateBD);
                  
                      
            String dateKT =  new SimpleDateFormat("yyyy-MM-dd").format(ngayKetThuc.getDate());
            java.sql.Date dateKT2 = java.sql.Date.valueOf(dateKT);
            
            Date dateNow = new Date();
                       
            
            String maVoucher = txt_MaVoucher.getText();
            String tenVoucher = txt_TenVoucher.getText();
            double mucGiam = Double.parseDouble(txt_mucGiam1.getText());
            double dieuKien = Double.parseDouble(txt_DieuKien.getText());
            int soLan = Integer.parseInt(txt_Solan.getText());
            
            if(checkMa(txt_MaVoucher.getText())){
                JOptionPane.showMessageDialog(rootPane, "Mã Giảm Giá đã tồn tại");
            }
            else if(tenVoucher.isEmpty()){
                JOptionPane.showMessageDialog(rootPane, "Tên Giảm giá không được để trống");
                txt_TenVoucher.requestFocus();
                check = false;
            }else if (checkTen(tenVoucher)){
                JOptionPane.showMessageDialog(rootPane, "Tên Giảm giá đã tồn tại !!");
                txt_TenVoucher.requestFocus();
                check = false;
            }
            
            else if(txt_mucGiam1.getText().isEmpty()){
                JOptionPane.showMessageDialog(rootPane, "Mức giảm giá không được để trống");
                txt_mucGiam1.requestFocus();
                check = false;
            }else if (!isDouble(txt_mucGiam1.getText())){
                JOptionPane.showMessageDialog(rootPane, "Dữ liệu nhập vào ở mức giảm phải là kiểu số ");
                txt_mucGiam1.requestFocus();
                check = false;
            }
            else if (mucGiam < 0 || mucGiam > 100){
                JOptionPane.showMessageDialog(rootPane, "Mức giảm nằm trong khoảng từ 0 đến 100");
                txt_mucGiam1.requestFocus();
                check = false;
            }
            else if (txt_DieuKien.getText().isEmpty()){
                JOptionPane.showMessageDialog(rootPane, "Điều kiện giảm không được để trống");
                txt_DieuKien.requestFocus();
                check = false;
            }
            else if(!isDouble(txt_DieuKien.getText().trim())){
                
                JOptionPane.showMessageDialog(rootPane, "Dữ liệu nhập vào ở ô điều kiện phải là kiểu số ");
                txt_mucGiam1.requestFocus();
                check = false;
            }           
            else if(ngayBatDau.getDate() == null ){
                JOptionPane.showMessageDialog(rootPane, "Ngày bắt đầu không được để trống");
                ngayBatDau.requestFocus();
                check = false;
            }else if(ngayBatDau.getDate().before(dateNow)){
                JOptionPane.showMessageDialog(rootPane, "Không thể tạo ngày bắt đầu trước ngày hôm nay");
                ngayBatDau.requestFocus();
                check = false;
            }
            else if(ngayKetThuc.getDate() == null){
                JOptionPane.showMessageDialog(rootPane, "Ngày kết thúc không được để trống");
                ngayKetThuc.requestFocus();
                check = false;
            }else if(ngayKetThuc.getDate().before(ngayBatDau.getDate())){
                JOptionPane.showMessageDialog(rootPane, "Không thể tạo ngày kết thúc trước ngày bắt đầu ");
                ngayKetThuc.requestFocus();
                check = false;
            }
            if(check == true){ 
            int chon = JOptionPane.showConfirmDialog(this, "Bạn có muốn thêm không?");
            if(chon == 0){
            km.setMa_voucher(maVoucher);
            km.setTen_giam_gia(tenVoucher);
            km.setPhan_tram_giam_gia(mucGiam);
            km.setDieu_kien_ap_dung(dieuKien);
            km.setNgay_bat_dau(dateBD2);
            km.setNgay_het_han(dateKT2);
            km.setSo_lan_su_dung(soLan);
            km.setMo_ta(txt_MoTa.getText());
            try {
                repo.insert(km);
                getdata();
                loaddata();
                JOptionPane.showMessageDialog(rootPane, "Đã thêm Giảm giá thành công");
            } catch (SQLException ex) {
                Logger.getLogger(Form_KhuyenMai.class.getName()).log(Level.SEVERE, null, ex);
            }
            } else{
                 JOptionPane.showMessageDialog(this, "Không thêm!");
            }          
            }  
    }//GEN-LAST:event_btn_ThemActionPerformed

